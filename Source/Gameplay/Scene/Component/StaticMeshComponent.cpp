void Arg::StaticMeshComponent::Render(const RenderContext& renderContext)
{
	const Mat4 transform = GetOwner()->GetTransform().GetGlobalTransform();

	glUniformMatrix4fv(glGetUniformLocation(renderContext.Shader, "u_Model"), 1, GL_FALSE, Math::Ref(transform));
	glUniform3f(glGetUniformLocation(renderContext.Shader, "u_Color"), m_Color.x, m_Color.y, m_Color.z);

	TextureResource* texRes = renderContext.Content->GetResource<TextureResource>(m_TextureID);
	if(texRes != nullptr)
	{
		texRes->Load();

		glUniform1i(glGetUniformLocation(renderContext.Shader, "u_Texture0"), 0);
		glActiveTexture(GL_TEXTURE0);
		texRes->GetTexture()->Bind();
	}

	glBindVertexArray(m_VertexArrayHandle);
	glDrawElements(GL_TRIANGLES, 36, GL_UNSIGNED_INT, nullptr);
	glBindVertexArray(0);
}

